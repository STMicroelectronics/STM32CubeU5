/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  /* STM32U5xx HAL library initialization:
       - Configure the Flash prefetch
       - Configure the Systick to generate an interrupt each 1 msec
       - Set NVIC Group Priority to 3
       - Low Level Initialization
     */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  HAL_PWR_EnableBkUpAccess();

  /* Set LSE drive capability configuration */
  __HAL_RCC_LSEDRIVE_CONFIG(RCC_BDCR_LSEDRV_1);
  /* USER CODE END Init */

  /* Configure the System Power */
  SystemPower_Config();

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* Initialize LED2 and LED3 */
  BSP_LED_Init(LED2);
  BSP_LED_Init(LED3);

  /* Turn LED3 on */
  BSP_LED_On(LED3);

  /* Delay 5 seconds */
  HAL_Delay(5000);

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ICACHE_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
  /* Turn LED3 on */
  BSP_LED_Off(LED3);

  /* Suspend Tick increment */
  HAL_SuspendTick();
#if defined (CPU_SLEEP_MODE)
  /* Enter the CPU to SLEEP mode */
  HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_SLEEPENTRY_WFI) ;
#elif defined (SYSTEM_STOP0_MODE)
  /* Enter the SYSTEM to STOP 0 mode */
  HAL_PWR_EnterSTOPMode(PWR_MAINREGULATOR_ON, PWR_STOPENTRY_WFI);
#elif defined (SYSTEM_STOP1_MODE)
  /* Enter the SYSTEM to STOP 1 mode */
  HAL_PWREx_EnterSTOP1Mode(PWR_STOPENTRY_WFI);
#elif defined (SYSTEM_STOP2_MODE)
  /* Enter the SYSTEM to STOP 2 mode */
  HAL_PWREx_EnterSTOP2Mode(PWR_STOPENTRY_WFI);
#elif defined (SYSTEM_STOP3_MODE)
  /* STOP3 wake up global interrupt configuration */
  HAL_NVIC_SetPriority(PWR_S3WU_IRQn, 7, 7);
  HAL_NVIC_EnableIRQ(PWR_S3WU_IRQn);

  /* Enter the SYSTEM to STOP 3 mode */
  HAL_PWREx_EnterSTOP3Mode(PWR_STOPENTRY_WFI);
#elif defined (SYSTEM_STANDBY_MODE)
  /* Enter the SYSTEM to STANDBY mode */
  HAL_PWR_EnterSTANDBYMode();
#elif defined (SYSTEM_SHUTDOWN_MODE)
  /* Enter the SYSTEM to SHUTDOWN mode */
  HAL_PWREx_EnterSHUTDOWNMode();
#endif /* Power mode */
  /* Resume Tick increment */
  HAL_ResumeTick();

#if (!defined (SYSTEM_STANDBY_MODE) || !defined (SYSTEM_SHUTDOWN_MODE))
  /* Reset the system after wake up */
  HAL_NVIC_SystemReset();
#endif

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

