/**
  * @brief  SYSTICK callback.
  * @retval None
  */
void HAL_SYSTICK_Callback(void)
{
  if (NonSecureTimingDelay != 0U)
  {
    NonSecureTimingDelay--;
  }
  else
  {
    /* Toggle PH.07 (LED7) */
    HAL_GPIO_TogglePin(GPIOH, GPIO_PIN_7);
    NonSecureTimingDelay = NONSECURE_IO_TOGGLE_DELAY;
  }
}

