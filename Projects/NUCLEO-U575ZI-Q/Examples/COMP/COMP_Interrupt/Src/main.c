/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  /* STM32U5xx HAL library initialization:
       - Configure the Flash prefetch
       - Configure the Systick to generate an interrupt each 1 msec
       - Set NVIC Group Priority to 3
       - Low Level Initialization
     */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the System Power */
  SystemPower_Config();

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* Initialize LED1*/
  BSP_LED_Init(LED1);

  /* LED1 off */
  BSP_LED_Off(LED1);
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ICACHE_Init();
  MX_COMP1_Init();
  /* USER CODE BEGIN 2 */
  /* Start COMP1 */
  if(HAL_COMP_Start(&hcomp1) != HAL_OK)
  {
    /* Initialization Error */
    Error_Handler();
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    /* Insert 5 second delay */
    HAL_Delay(5000);

    /* Toggle LED1 4 times before entering in low-power mode */
    BSP_LED_Toggle(LED1);
    HAL_Delay(100);
    BSP_LED_Toggle(LED1);
    HAL_Delay(100);
    BSP_LED_Toggle(LED1);
    HAL_Delay(100);
    BSP_LED_Toggle(LED1);
    HAL_Delay(100);

    /* PWR clock enable */
    __HAL_RCC_PWR_CLK_ENABLE();

    /* Enter the system to stop 2 mode */
    HAL_PWREx_EnterSTOP2Mode(PWR_STOPENTRY_WFI);

    /* Check that the system was resumed from stop 2 */
    if(__HAL_PWR_GET_FLAG(PWR_FLAG_STOPF) == 0U)
    {
      /* Initialization Error */
      Error_Handler();
    }

    /* Clear stop flag */
    __HAL_PWR_CLEAR_FLAG(PWR_FLAG_STOPF);

    /* Check that stop flag is cleared */
    if (__HAL_PWR_GET_FLAG(PWR_FLAG_STOPF) != 0U)
    {
      /* Initialization Error */
      Error_Handler();
    }

    /* Re-configure the system clock to 160 MHz, enable and
    select PLL as system clock source (PLL is disabled in STOP mode) */
    SystemClock_Restore();
  }
  /* USER CODE END 3 */
}

